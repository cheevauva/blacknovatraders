<A NAME=techlevels></A><H2>Уровни технологий:</H2>
Вы можете улучшить компоненты вашего корабля в любом специальном порту. Каждое улучшение компонента улучшает характеристики и возможности вашего корабля.
<BR><BR>
<B>Корпус:</B><BR>
Определяет количество отсеков, доступных на вашем корабле (для перевозки товаров и колонистов).
<BR><BR>
<B>Двигатели:</B><BR>
Определяет размер ваших двигателей. Более крупные двигатели могут перемещаться в реальном пространстве быстрее.
<BR><BR>
<B>Энергия:</B><BR>
Определяет количество энергии, которое может нести ваш корабль.
<BR><BR>
<B>Компьютер:</B><BR>
Определяет количество истребителей, которыми ваш корабль может управлять.
<BR><BR>
<B>Сенсоры:</B><BR>
Определяет точность ваших сенсоров при сканировании корабля или планеты. Успех сканирования зависит от уровня маскировки цели.
<BR><BR>
<B>Броня:</B><BR>
Определяет количество очков брони, которые может использовать ваш корабль.
<BR><BR>
<B>Щиты:</B><BR>
Определяет эффективность системы щитов вашего корабля во время боя.
<BR><BR>
<B>Лучевое оружие:</B><BR>
Определяет эффективность лучевого оружия вашего корабля во время боя.
<BR><BR>
<B>Торпедные установки:</B><BR>
Определяет количество торпед, которые может использовать ваш корабль.
<BR><BR>
<B>Маскировка:</B><BR>
Определяет эффективность системы маскировки вашего корабля. Подробнее см. «Сенсоры».
<BR><BR>
<A NAME=devices></A><H2>Устройства:</H2>
<B>Космические маяки:</B><BR>
Разместите предупреждение или сообщение, которое будет отображаться любому, кто войдёт в этот сектор. Только 1 маяк может быть активен в каждом секторе, поэтому новый маяк удаляет существующий (если есть).
<BR><BR>
<B>Варп-редакторы:</B><BR>
Создавайте или уничтожайте варп-связи с другим сектором.
<BR><BR>
<B>Генезис-торпеды:</B><BR>
Создают планету в текущем секторе (если её ещё нет).
<BR><BR>
<B>Отклонятель мин:</B><BR>
Защищает игрока от мин, размещённых в космосе. Каждый отклонятель уничтожает 1 мину.
<BR><BR>
<B>Устройство аварийного варпа:</B><BR>
Перемещает ваш корабль в случайный сектор при ручной активации. В противном случае, устройство аварийного варпа может защитить ваш корабль при атаке, переместив вас за пределы досягаемости атакующего.
<BR><BR>
<B>Спасательная капсула (максимум 1):</B><BR>
Позволяет вам выжить при уничтожении вашего корабля, давая возможность сохранить ваши кредиты и планеты.
<BR><BR>
<B>Топливный ковш (максимум 1):</B><BR>
Накопите единицы энергии при использовании перемещения в реальном пространстве.
<BR><BR>
<A NAME=zones></A><H2>Зоны:</H2>
Галактика разделена на различные области, в каждой из которых действуют разные правила. Чтобы отобразить ограничения, прикреплённые к вашему текущему сектору, просто нажмите на название зоны (верхний правый угол главной страницы). Ваш корабль может быть отбуксирован из зоны в случайный сектор, если размер корпуса превышает максимально разрешённый уровень для этой конкретной зоны. Атаки на других игроков и использование некоторых устройств также могут быть запрещены в некоторых зонах.
