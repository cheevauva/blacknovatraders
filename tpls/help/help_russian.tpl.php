<A NAME=mainmenu></A><H2>Команды главного меню:</H2>
<B>Отчёт о корабле:</B><BR>
Отображает детальный отчёт о системах вашего корабля, грузе и вооружении. Вы можете вызвать этот отчёт, нажав на имя вашего корабля в верхней части главной страницы.
<BR><BR>
<B>Варп-переходы:</B><BR>
Перемещайтесь из одного сектора в другой через варп-связи, нажимая на номера секторов.
<BR><BR>
<B>Дальнее сканирование:</B><BR>
Просканируйте соседний сектор с помощью ваших дальних сканеров, фактически не перемещаясь туда.
<?php if ($allow_fullscan): ?>
    Полное сканирование даст вам обзор всех соседних секторов за один широкий проход ваших сенсоров.
<?php endif; ?>
<BR><BR>
<B>Корабли:</B><BR>
Просканируйте или атакуйте корабль (если он появился на ваших сенсорах), нажав на соответствующую ссылку справа от имени корабля. Атакованный корабль может уклониться от вашей атаки в зависимости от его технологических уровней.
<BR><BR>
<B>Торговые порты:</B><BR>
Получите доступ к меню торговли порта, нажав на тип порта, когда вы входите в сектор, где он присутствует.
<BR><BR>
<B>Планеты:</B><BR>
Получите доступ к меню планеты, нажав на её название, когда вы входите в сектор, где она присутствует.
<BR><BR>
<?php if ($allow_navcomp): ?>
    <B>Навигационный компьютер:</B><BR>
    Используйте ваш компьютер, чтобы найти маршрут к определённому сектору. Мощность навигационного компьютера зависит от уровня технологии ваших компьютеров.
    <BR><BR>
<?php endif; ?>
<B>Реальное пространство:</B><BR>
Используйте двигатели вашего корабля, чтобы добраться до определённого сектора. Улучшите уровень технологии двигателей, чтобы эффективно использовать перемещения в реальном пространстве. Нажав на ссылку «Предустановки», вы можете запомнить до 3 номеров секторов для быстрого перемещения или можете указать любой сектор, используя ссылку «Другой».
<BR><BR>
<B>Торговые пути:</B><BR>
Используйте торговые пути для быстрой торговли товарами между портами. Торговые пути используют перемещения в реальном пространстве, чтобы двигаться туда и обратно между двумя портами и торговать максимальным количеством товаров на каждом конце. Убедитесь, что удалённый сектор содержит торговый порт, прежде чем использовать торговый путь. Предустановки торговых путей общие с предустановками перемещений в реальном пространстве. Как и при перемещениях в реальном пространстве, любой сектор может быть выбран с помощью ссылки «Другой».
<BR><BR>
<H3>Панель меню (нижняя часть главной страницы):</H3>
<B>Устройства:</B><BR>
Используйте различные устройства, которые несёт ваш корабль (Генезис-торпеды, маяки, Варп-редакторы и т.д.). Для более подробной информации о каждом устройстве прокрутите вниз до раздела «Устройства».
<BR><BR>
<B>Планеты:</B><BR>
Отображает список всех ваших планет с текущими суммами товаров, вооружения и кредитов.
<BR><BR>
<B>Журнал:</B><BR>
Отображает журнал событий, произошедших с вашим кораблём.
<BR><BR>
<B>Отправить сообщение:</B><BR>
Отправьте электронное письмо другому игроку.
<BR><BR>
<B>Рейтинги:</B><BR>
Отображает список лучших игроков, ранжированных по их текущим очкам.
<BR><BR>
<B>Последние пользователи:</B><BR>
Отображает список пользователей, которые недавно вошли в игру.
<BR><BR>
<B>Настройки:</B><BR>
Изменяйте пользовательские настройки (в настоящее время можно изменить только пароль).
<BR><BR>
<B>Обратная связь:</B><BR>
Отправьте электронное письмо администратору игры.
<BR><BR>
<B>Самоуничтожение:</B><BR>
Уничтожьте свой корабль и удалите себя из игры.
<BR><BR>
<B>Помощь:</B><BR>
Отображает страницу помощи (то, что вы сейчас читаете).
<BR><BR>
<B>Выйти:</B><BR>
Удаляет все игровые куки из вашей системы, завершая текущую сессию.
